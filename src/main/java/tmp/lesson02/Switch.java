package tmp.lesson02;

public class Switch {
    public static void main(String[] args) {
        int a = 1;
        switch (a) {
            case 1 :
                System.out.println("11111");
                break;
            case 2:
                System.out.println("2222");
                break;
            default:
                System.out.println("OLOLO");
        }
    }


}
